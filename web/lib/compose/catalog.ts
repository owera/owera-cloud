// The catalog of 100 hireable jobs.
//
// Each entry is a concrete deliverable an SMB can name in one breath. The
// jobs map 1:1 to the research brief on SMB use cases (see PR description).
//
// What's in an entry:
//   - id              stable slug for URLs and analytics
//   - function        which department it belongs to
//   - name            short, human, outcome-shaped name
//   - tagline         one-line "what you get" (display italic in the UI)
//   - killer          true if SMBs would pay $50–$500/mo without flinching
//   - trigger         when it runs (schedule / event / inbox / manual / threshold)
//   - sources         what it reads (1+ connectors)
//   - action          what it does (verb + plain-language description)
//   - output          where the result lands
//   - approval        default autonomy gate
//   - memory          what it remembers across runs
//   - sla             when results are needed
//   - priceMonthly    suggested subscription price band, in USD cents
//   - billingUnit     the "unit of value" we charge per accepted result
//   - heroPrompt      a synthesized prompt seed; the agent receives this on first run

import type {
  Action,
  Approval,
  Memory,
  Output,
  Source,
  Trigger,
} from "./primitives";
import type { FunctionId } from "./functions";

export interface JobBlueprint {
  id: string;
  function: FunctionId;
  name: string;
  tagline: string;
  killer: boolean;
  trigger: Trigger;
  sources: ReadonlyArray<Source>;
  action: Action;
  output: Output;
  approval: Approval;
  memory: Memory;
  sla: string;
  priceMonthly: number;
  billingUnit: string;
  heroPrompt: string;
}

const T = {
  daily: (at: string): Trigger => ({ kind: "schedule", cadence: `Every day at ${at}` }),
  weekly: (day: string, at: string): Trigger => ({
    kind: "schedule",
    cadence: `Every ${day} at ${at}`,
  }),
  monthly: (day: string, at: string): Trigger => ({
    kind: "schedule",
    cadence: `On the ${day} of each month at ${at}`,
  }),
  quarterly: (when: string): Trigger => ({
    kind: "schedule",
    cadence: `Quarterly — ${when}`,
  }),
  hourly: (): Trigger => ({ kind: "schedule", cadence: "Every hour" }),
  event: (signal: string): Trigger => ({ kind: "event", signal }),
  inbox: (signal: string): Trigger => ({ kind: "inbox", signal }),
  threshold: (predicate: string): Trigger => ({ kind: "threshold", predicate }),
  manual: (): Trigger => ({ kind: "manual" }),
};

const src = (category: Source["category"], connector: string, label?: string): Source => ({
  category,
  connector,
  ...(label ? { label } : {}),
});

const A = (verb: Action["verb"], description: string): Action => ({ verb, description });

const O = (kind: Output["kind"], target?: string): Output => ({
  kind,
  ...(target ? { target } : {}),
});

const APP = {
  draft: { kind: "draft-for-approval" } as Approval,
  auto: { kind: "autonomous" } as Approval,
  ask: { kind: "always-ask" } as Approval,
  gated: (threshold: number): Approval => ({ kind: "confidence-gated", threshold }),
};

const MEM = {
  stateless: { kind: "stateless" } as Memory,
  perTarget: (desc: string): Memory => ({ kind: "per-target", description: desc }),
  perJob: (desc: string): Memory => ({ kind: "per-job", description: desc }),
  org: (desc: string): Memory => ({ kind: "org-persistent", description: desc }),
};

