export function getJobBlueprint(id: string): JobBlueprint | undefined {
  return JOB_CATALOG.find((j) => j.id === id);
}

export function jobsByFunction(fid: FunctionId): JobBlueprint[] {
  return JOB_CATALOG.filter((j) => j.function === fid);
}

export function killerJobs(): JobBlueprint[] {
  return JOB_CATALOG.filter((j) => j.killer);
}
